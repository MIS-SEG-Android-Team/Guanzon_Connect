package org.rmj.guanzongroup.marketplace.Model;

public class WishListModel {

    private String wishImage;
    private String wishName;
    private String wishPrice;
    private String wishPriceDrop;
    private String wishPercent;
    public WishListModel(){}

    public String getWishImage() {
        return wishImage;
    }

    public void setWishImage(String wishImage) {
        this.wishImage = wishImage;
    }

    public String getWishName() {
        return wishName;
    }

    public void setWishName(String wishName) {
        this.wishName = wishName;
    }

    public String getWishPrice() {
        return wishPrice;
    }

    public void setWishPrice(String wishPrice) {
        this.wishPrice = wishPrice;
    }

    public String getWishPriceDrop() {
        return wishPriceDrop;
    }

    public void setWishPriceDrop(String wishPriceDrop) {
        this.wishPriceDrop = wishPriceDrop;
    }

    public String getWishPercent() {
        return wishPercent;
    }

    public void setWishPercent(String wishPercent) {
        this.wishPercent = wishPercent;
    }

}
